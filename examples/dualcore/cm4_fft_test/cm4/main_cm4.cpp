/* cm4_fft_test CM4 firmware: CMSIS-DSP known-answer tests ON THE M4.
 * Port of examples/framework/arm_math_test/arm_math_test.cpp's three stages
 * to the freestanding CM4 image world (no Serial, no libm, no libc):
 *   FFT  — 256-pt radix-4 complex q15 FFT of a hardcoded cos(2*pi*8*i/256)
 *          input at 0.5 FS; all energy must land in bin 8;
 *   FIR  — unit impulse through an 8-tap q15 FIR echoes the coefficients;
 *   SIN  — arm_sin_q31 at 5 fixed table-point angles vs precomputed q31
 *          values (replaces the CM7 test's libm sinf sweep: sinTable_q31 is
 *          exactly round(sin(2*pi*k/512)*2^31) and interpolation is exact at
 *          table points, so a +/-215-count tolerance is both generous and
 *          far below the CM7 test's 1e-4 bar).
 * The image stays -nostdlib: input/expected tables are generated offline
 * (one-liners in comments below) instead of calling cosf/sinf at runtime.
 *
 * Reported over MU channel 0, in order:
 *   fft_bin   best bin 1..127 (0xFFFFFFFF if the 4*second-peak margin failed)
 *   fft_mag2  re^2+im^2 of the best bin
 *   fir_ok    1/0
 *   sin_ok    1/0
 *   done      0xD0DE0004
 * CM7 relay asserts fft_bin==8, fft_mag2 in [5e7,9e7], fir_ok, sin_ok.
 *
 * NOTE: the shared startup_cm4.S enters `main` with PRIMASK set — irrelevant
 * here, no interrupts are used. Public domain (N. Newdigate). */
#include <stdint.h>
#include <arm_math.h>
extern "C" {
#include "mu_report.h"
void cm4_run_ctors(void);
}

